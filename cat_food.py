cats = 10
cans_per_day = 0.75
days = 7
total_cans = cats * cans_per_day * days
print("each day you need " + str(total_cans) + " cans" + " to feed " + str(cats) + ' cats')
print('for ' + str(days) + ' days' + ' you need ' + str(total_cans) + ' cans ' + 'to feed ' + str(cats) + ' cats')
print('for {} days you need {} cans to feed {} cats'.format(days, total_cans, cats) )













